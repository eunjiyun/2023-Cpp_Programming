#include<iostream>
#include "save.h"
using namespace std;

class Dog {
	string name;
	int age;
public:
	Dog(){}
	Dog(string str, int num) :name(str),age(num){
		cout << name << " 탄생" << endl;
	}
	~Dog() {
		cout << name << "개님 물러가심" << endl;
		//PlaySound();
	}

	//복사생성자
	Dog(const Dog& other) :name{ other.name }, age{ other.age } {
		cout << name << " 복사생성" << endl;
	}
};


//---------
int main()
//---------
{
	Dog d{ "댕댕",2 };//디폴트 생성
	Dog x(d);//x는 d를 복사해서 생성
	Dog y{ "와이",20 };

	save("소스.cpp");
}

